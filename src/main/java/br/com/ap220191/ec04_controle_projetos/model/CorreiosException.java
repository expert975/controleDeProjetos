package br.com.ap220191.ec04_controle_projetos.model;

public class CorreiosException extends Exception
{

	CorreiosException()
	{
		super();
	}

	CorreiosException(Throwable cause)
	{
		super(cause);
	}

	CorreiosException(String message)
	{
		super(message);
	}

	CorreiosException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
